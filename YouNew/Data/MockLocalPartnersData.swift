    static func partners(in city: String) -> [LocalPartner] {
        partners.filter { $0.city.caseInsensitiveCompare(city) == .orderedSame }
    }

    static func partner(id: String) -> LocalPartner? {
        partners.first { $0.id == id }
    }

    static func matching(query: String, city: String?) -> [LocalPartner] {
        let normalized = query.folding(options: [.caseInsensitive, .diacriticInsensitive], locale: Locale(identifier: "en_US_POSIX")).lowercased()
        return partners.filter { partner in
            let cityMatches = city.map { partner.city.caseInsensitiveCompare($0) == .orderedSame } ?? true
            let text = [
                partner.name,
                partner.category.title(.english),
                partner.subcategory,
                partner.description,
                partner.address
            ].joined(separator: " ").folding(options: [.caseInsensitive, .diacriticInsensitive], locale: Locale(identifier: "en_US_POSIX")).lowercased()
            return cityMatches && text.containsAnyToken(from: normalized)
        }
        .sorted { lhs, rhs in
            partnerRank(lhs.plan) < partnerRank(rhs.plan)
        }
    }

    private static func partnerRank(_ plan: LocalPartnerPlan) -> Int {
        switch plan {
        case .aiFeatured: return 0
        case .premium: return 1
        case .featured: return 2
        case .verifiedPartner: return 3
        case .sponsoredPlacement: return 4
        case .freeListing: return 5
        }
    }
}

private extension String {
    func containsAnyToken(from query: String) -> Bool {
        guard !query.isEmpty else { return true }
        if contains(query) { return true }
        let tokens = query.split(separator: " ").map(String.init)
        return tokens.contains { token in
            token.count > 2 && contains(token)
        }
    }
