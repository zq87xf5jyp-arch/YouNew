import Foundation

struct L10n {

    // MARK: - File-based cache (loads all 486+ keys from .strings files)

    private static var _fileCache: [AppLanguage: [String: String]] = [:]

    private static func fileStrings(for language: AppLanguage) -> [String: String] {
        if let cached = _fileCache[language] { return cached }
        let dir = "\(language.rawValue).lproj"
        let dict: [String: String]
        if let path = Bundle.main.path(forResource: "Localizable", ofType: "strings", inDirectory: dir),
           let raw = NSDictionary(contentsOfFile: path) as? [String: String] {
            dict = raw
        } else {
            dict = [:]
        }
        _fileCache[language] = dict
        return dict
    }

    // MARK: - Public API

    static func t(_ key: String, _ language: AppLanguage) -> String {
        // 1. Try selected language from .strings file
        let target = fileStrings(for: language)
        if let v = target[key] { return v }
        // 2. Fall back to English .strings
        if language != AppLanguage.english {
            let en = fileStrings(for: AppLanguage.english)
            if let v = en[key] { return v }
        }
        // 3. Last resort: hardcoded fallback dict
        let fallback: String?
        switch language {
        case AppLanguage.english:  fallback = _en[key]
        case AppLanguage.dutch:    fallback = _nl[key] ?? _en[key]
        case AppLanguage.russian:  fallback = _ru[key] ?? _en[key]
        }
        if let fallback { return fallback }
        return key
    }

